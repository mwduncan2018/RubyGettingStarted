names = ["Mike", "Erik", "Stephanie", 1, nil, "Travis", "Gail", "Eric"]

#p is for debugging
puts "===p==="
p names
puts ""

#puts displays output for the user with a newline at the end
puts "===puts==="
puts names
puts ""

#print displays output without a newline at the end
puts "===print==="
print names
puts "This will continue on the same line"
puts ""

#pp seems to handle arrays the same way as p
puts "===pp==="
pp names
puts ""